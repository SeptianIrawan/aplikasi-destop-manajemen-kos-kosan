    private void tampilTotalHargaSewaHariIni() {
   try {
    // Pastikan koneksi database valid
    if (con == null || con.isClosed()) {
        JOptionPane.showMessageDialog(rootPane, "Koneksi database tidak valid.");
        return;
    }

    // Query untuk menjumlahkan harga dari tbbulanan berdasarkan created_at hari ini
    String sql = "SELECT SUM(harga) AS total_harga_sewa " +
                 "FROM tbbulanan " +
                 "WHERE DATE(created_at) = CURDATE()"; // Ambil data hanya untuk hari ini

    // Siapkan PreparedStatement
    PreparedStatement pstmt = con.prepareStatement(sql);
    
    // Cetak query untuk debugging
    System.out.println("Query: " + sql);
    
    // Eksekusi query
    ResultSet rs = pstmt.executeQuery();

    // Jika data ditemukan, tampilkan total harga sewa pada JLabel
    if (rs.next()) {
        BigDecimal totalHargaSewa = rs.getBigDecimal("total_harga_sewa");
        if (totalHargaSewa != null) {
            hargasewa.setText("Rp " + totalHargaSewa.toPlainString());
        } else {
            hargasewa.setText("Rp. 0");
        }
    } else {
        hargasewa.setText("Rp. 0");
        System.out.println("Tidak ada data transaksi hari ini."); // Debugging
    }

    // Tampilkan log jika perlu (opsional)
    System.out.println("Total harga sewa dari tbbulanan hari ini berhasil dimuat.");

} catch (Exception e) {
    // Tangani kesalahan SQL
    JOptionPane.showMessageDialog(rootPane, "Gagal memuat total harga sewa: " + e.getMessage());
    e.printStackTrace(); // Cetak stack trace untuk debugging
}

}
