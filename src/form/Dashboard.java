   private void tampilkanJumlahKamar() {
    if (stat == null) {
        JOptionPane.showMessageDialog(this, "Koneksi database belum berhasil. Tidak dapat menampilkan jumlah kamar.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
        return;
    }

    try {
        // Query untuk menghitung jumlah total kamar
        String queryTotal = "SELECT COUNT(*) AS total FROM tbkamar";
        rs = stat.executeQuery(queryTotal); // Eksekusi query total kamar
        if (rs.next()) {
            int totalData = rs.getInt("total"); // Ambil hasil perhitungan total kamar
            datakamar.setText("" + totalData); // Tampilkan di label jumlah kamar
        }

        // Query untuk menghitung jumlah kamar kosong
        String queryKosong = "SELECT COUNT(*) AS totalKosong FROM tbkamar WHERE status = 'tersedia'";
        rs = stat.executeQuery(queryKosong); // Eksekusi query kamar kosong
        if (rs.next()) {
            int totalKosong = rs.getInt("totalKosong"); // Ambil hasil perhitungan kamar kosong
            kamarkosong.setText("" + totalKosong); // Tampilkan di label kamar kosong
        }

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Kesalahan", JOptionPane.ERROR_MESSAGE);
        datakamar.setText("Error menampilkan jumlah data");
        kamarkosong.setText("Error menampilkan kamar kosong");
    }
}


